// Render banners
function renderGallery(images) {
  bannerGrid.innerHTML = '';
  images.forEach(src => {
    const img = document.createElement("img");
    img.src = src;
    bannerGrid.appendChild(img);
  });
}

// Update gallery automatically
function updateGallery() {
  const year = yearSelect.value;
  const month = monthSelect.value;
  const key = `${year}-${month}`;
  if (bannersDatabase[key]) {
    renderGallery(bannersDatabase[key]);
  } else {
    // Show all banners if no match
    renderGallery(Object.values(bannersDatabase).flat());
  }
}
yearSelect.addEventListener("change", updateGallery);
monthSelect.addEventListener("change", updateGallery);

